/** 全国任意城市/区县，通过高德地理编码解析 */
export interface CityInfo {
  name: string;
  adcode: string;
  /** 地级市 adcode（区县级向上归并） */
  cityAdcode: string;
  province: string;
  lat: number;
  lng: number;
  formattedAddress: string;
}

export function toCityLevelAdcode(adcode: string): string {
  if (adcode.length >= 6) {
    const prefix = adcode.slice(0, 4);
    // 直辖市/地级市
    if (adcode.endsWith("0000")) return adcode;
    return `${prefix}00`;
  }
  return adcode;
}

